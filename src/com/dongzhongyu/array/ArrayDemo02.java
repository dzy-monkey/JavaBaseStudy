package com.dongzhongyu.array;

//遍历一个数组中的所有元素，并输出出来
public class ArrayDemo02 {
    public static void main(String[] args) {
        int[] scoreArray = new int[]{1, 2, 3, 4, 5, 6, 7, 8};
        for (int i = 0; i < scoreArray.length; i++) {
            System.out.println(scoreArray[i]);
        }
    }
}
